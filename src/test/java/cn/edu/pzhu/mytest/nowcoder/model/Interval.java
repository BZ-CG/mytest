package cn.edu.pzhu.mytest.nowcoder.model;

/**
 * 区间
 *
 * @author zhangcz
 * @since 20230627
 */
public class Interval {
    public int start;
    public int end;

    public Interval() {
        start = 0;
        end = 0;
    }

    public Interval(int s, int e) {
        start = s;
        end = e;
    }
}
